int main() {
  LL x,y;
  cin>>x>>y;
  LL cnt = 0;
  if(abs(x)<abs(y)){
    cnt+=x>=0?0:1;
    x=max(x,-x);
    cnt+=abs(y)-abs(x);
    cnt+=y>=0?0:1;
  }else{
    cnt+=x<=0?0:1;
    x=min(x,-x);
    cnt+=abs(x)-abs(y);
    cnt+=y<=0?0:1;
  }
  cout<<cnt<<endl;

  return 0;
}
