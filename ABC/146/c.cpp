int main() {
  LL A, B, X;
  cin >> A >> B >> X;
  int left = 0;
  int right = 1e9 + 1;
  auto f = [&](LL N) -> LL {
    int keta = 0;
    LL M = N;
    while (M > 0) {
      M /= 10;
      keta++;
    }
    return A * N + B * keta;
  };
  while (right - left > 1) {
    LL mid = (left + right) / 2;
    // DUMP(left, right, f(mid));
    if (f(mid) <= X) {
      left = mid;
    } else {
      right = mid;
    }
  }
  cout << left << endl;

  return 0;
}
