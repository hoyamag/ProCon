const int lim = 1e6 + 1;
int dp[lim];
LL dfs_count(LL H) {
  if (H == 1) {
    return 1;
  }
  if (H < lim) {
    if (dp[H] != -1) {
      return dp[H];
    } else {
      dp[H] = 1 + 2 * dfs_count(H / 2);
    }
    return dp[H];
  } else {
    return 1 + 2 * dfs_count(H / 2);
  }
}

